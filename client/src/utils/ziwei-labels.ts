/** 궁 해석 부제 — 예: (내면/정신) */
export function formatPalaceTheme(hanjaPalaceName: string): string {
  const theme = PALACE_THEME[hanjaPalaceName]
  return theme ? `(${theme})` : ''
}

export function ziweiKor(hanja: string): string | undefined {
  return ZIWEI_KOR_MAP[hanja]
}

/** 한 줄: 한글(한자) */
export function formatZiweiInline(hanja: string): string {
  const kor = ziweiKor(hanja)
  return kor ? `${kor}(${hanja})` : hanja
}

/** AI 복사용 — 미매핑은 한자 그대로 */
export function formatZiweiInlineOrHanja(hanja: string): string {
  return formatZiweiInline(hanja)
}
