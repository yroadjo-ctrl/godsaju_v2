const ganziIndex = new Map(HGANJI.map((g, i) => [g, i]));

export interface NayeonInfo {
  hanja: string;
  hangul: string;
}

export function getNayeon(ganzi: string): NayeonInfo | null {
  const idx = ganziIndex.get(ganzi);
  if (idx === undefined) return null;
  const [hanja, hangul] = NAYEON_TABLE[idx];
  return { hanja, hangul };
}

/** 표·복사용: 해중금(海中金) */
export function formatNayeon(ganzi: string): string {
  const n = getNayeon(ganzi);
  if (!n) return '-';
  return `${n.hangul}(${n.hanja})`;
}
